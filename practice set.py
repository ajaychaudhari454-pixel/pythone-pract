# This program compares two numbers
# and checks whether the first number is greater than the second.

# Taking first number input from the user and converting it to integer
a = int(input("Enter a number: "))

# Taking second number input from the user and converting it to integer
b = int(input("Enter another number: "))

# Comparing both numbers using greater than (>) operator
# This will return True if a is greater than b, otherwise False
print("a is greater than b:", a > b)
